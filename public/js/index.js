const getInput = () => {
  const coo = document.getElementById("coo").value;
  if (country.indexOf(coo) === -1) {
    return alert("Put the name of your Country properly");
  }
  fetch("/api/getData/" + iso_to_gcal_description[country.indexOf(coo)], {
    method: "GET"
  })
    .then((data) => {
      data.json().then((data) => {
        if (data.message === "No Holiday Today") {
          document.getElementById("message").innerText = data.message;
          document.getElementById("message").style.display = "block";
        } else {
          document.getElementById("message").innerText = data.message;
          document.getElementById("message").style.display = "block";
          document.getElementById("sengr").style.display = "block";
          document.getElementById("moge").style.display = "none";
        }
      });
    })
    .catch((err) => {});
};

const send = () => {
  const emid = document.getElementById("emid").value;
  const yname = document.getElementById("yname").value;
  const rname = document.getElementById("rname").value;
  fetch("/api/send/", {
    method: "POST",
    headers: { "content-type": "application/json" },
    body: JSON.stringify({ id: emid, yname: yname, rname: rname })
  }).then((data) => {
    data.json().then((data) => {
      alert(data.message);
    });
  });
};
